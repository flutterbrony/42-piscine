void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int buff[21];
	int i;

	i = 0;
	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	}
	while (nb > 0)
	{
		buff[i] = nb % 10;
		i++;
		nb = nb / 10;
	}
	i--;
	while (i >= 0)
	{
		ft_putchar(buff[i] + 48);
		i--;
	}
}
