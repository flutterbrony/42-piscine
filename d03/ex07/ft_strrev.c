char	*ft_strrev(char *str)
{
	char	swap;
	int		i;
	int		j;

	j = 0;
	i = 0;
	while (str[j])
	{
		j++;
	}
	while (i < j / 2)
	{
		swap = str[i];
		str[i] = str[j - i - 1];
		str[j - i - 1] = swap;
		i++;
	}
	return (str);
}
